/**
 * Strip "Chapter N:", "Chapter One:", "Ch. N:", Roman numerals like "I:",
 * etc. from the front of a title so old LLM-generated data renders clean.
 *
 *   "Chapter I: The Founding"      → "The Founding"
 *   "CHAPTER 1: The Everything…"    → "The Everything…"
 *   "Ch. 2 - A Reckoning"           → "A Reckoning"
 *   "II. The Reign"                 → "The Reign"
 *   "The Founding"                  → "The Founding"  (unchanged)
 */
export function stripChapterPrefix(raw: string): string {
  if (!raw) return raw;
  let t = raw.trim();

  // "Chapter N:", "Chapter One:", "Ch N:", "Ch. N:", optional "-" or ":" separator
  const chapterRe =
    /^\s*(?:chapter|ch\.?)\s+(?:\d+|[ivxlcdm]+|one|two|three|four|five|six|seven|eight|nine|ten|eleven|twelve)\s*[:\-–—.]?\s*/i;
  if (chapterRe.test(t)) {
    t = t.replace(chapterRe, "").trim();
  } else {
    // Standalone leading roman numeral + separator: "I:", "IV.", "XII —"
    const romanRe = /^\s*[IVXLCDM]{1,6}\s*[:\-–—.]\s*/;
    if (romanRe.test(t)) {
      t = t.replace(romanRe, "").trim();
    } else {
      // Leading bare arabic numeral: "1. The Founding", "2: Onward"
      const arabicRe = /^\s*\d{1,3}\s*[:\-–—.]\s*/;
      if (arabicRe.test(t)) {
        t = t.replace(arabicRe, "").trim();
      }
    }
  }

  // Catch "Being a Chronicle", "A Chronicle of …" style hardcoded-theme leftovers
  t = t.replace(/^\s*being\s+a\s+chronicle\b[:\-–—.,]?\s*/i, "").trim();

  return t || raw; // if stripping left nothing, use original
}

/**
 * Reveal the outer gold frame from top → right → bottom → left using a
 * polygon clip-path. At progress=0 it's invisible; at progress=1 it's full.
 */
function paintedFrameMask(progress: number): string {
  const p = Math.max(0, Math.min(1, progress));
  if (p <= 0) {
    return "polygon(0 0, 0 0, 0 0, 0 0)";
  }
  if (p >= 1) {
    return "polygon(0 0, 100% 0, 100% 100%, 0 100%)";
  }
  // Four-stage reveal: top (0-0.25), right (0.25-0.5), bottom (0.5-0.75), left (0.75-1)
  const topPct = Math.min(1, p / 0.25) * 100;
  const rightPct = Math.min(1, Math.max(0, (p - 0.25) / 0.25)) * 100;
  const bottomPct = Math.min(1, Math.max(0, (p - 0.5) / 0.25)) * 100;
  const leftPct = Math.min(1, Math.max(0, (p - 0.75) / 0.25)) * 100;
  return `polygon(
    0 0,
    ${topPct}% 0,
    ${topPct}% 0,
    100% 0,
    100% ${rightPct}%,
    100% ${rightPct}%,
    100% 100%,
    ${100 - bottomPct}% 100%,
    ${100 - bottomPct}% 100%,
    0 100%,
    0 ${100 - leftPct}%,
    0 ${100 - leftPct}%
  )`;
}

function computeTitleFont(title: string, pageW: number, portrait: boolean): number {
  const len = title.trim().length;
  const base = portrait ? pageW * 0.09 : pageW * 0.08;
  if (len <= 8) return base * 1.15;
  if (len <= 14) return base;
  if (len <= 22) return base * 0.82;
  if (len <= 32) return base * 0.66;
  if (len <= 46) return base * 0.54;
  if (len <= 64) return base * 0.46;
  return base * 0.4;
}

function darkenHex(hex: string, amt: number): string {
  const h = hex.replace("#", "");
  const full = h.length === 3 ? h.split("").map((c) => c + c).join("") : h;
  const num = parseInt(full, 16);
  let r = (num >> 16) - Math.round(255 * amt);
  let g = ((num >> 8) & 0xff) - Math.round(255 * amt);
  let b = (num & 0xff) - Math.round(255 * amt);
  r = Math.max(0, Math.min(255, r));
  g = Math.max(0, Math.min(255, g));
  b = Math.max(0, Math.min(255, b));
  return "#" + ((1 << 24) | (r << 16) | (g << 8) | b).toString(16).slice(1);
}

function lightenHex(hex: string, amt: number): string {
  const h = hex.replace("#", "");
  const full = h.length === 3 ? h.split("").map((c) => c + c).join("") : h;
  const num = parseInt(full, 16);
  let r = (num >> 16) + Math.round(255 * amt);
  let g = ((num >> 8) & 0xff) + Math.round(255 * amt);
  let b = (num & 0xff) + Math.round(255 * amt);
  r = Math.max(0, Math.min(255, r));
  g = Math.max(0, Math.min(255, g));
  b = Math.max(0, Math.min(255, b));
  return "#" + ((1 << 24) | (r << 16) | (g << 8) | b).toString(16).slice(1);
}

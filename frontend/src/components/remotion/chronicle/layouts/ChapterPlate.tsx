/**
 * Body paragraph with its first letter slightly enlarged and accented —
 * a subtle homage to the illuminated-initial tradition without shouting.
 */
const BodyWithFirstLetter: React.FC<{ text: string; accent: string }> = ({
  text,
  accent,
}) => {
  const trimmed = text.trim();
  if (!trimmed) return null;
  const first = trimmed.charAt(0);
  const rest = trimmed.slice(1);
  return (
    <>
      <span
        style={{
          fontStyle: "normal",
          fontWeight: 800,
          fontSize: "1.35em",
          color: accent,
          marginRight: "0.04em",
          letterSpacing: "0.01em",
        }}
      >
        {first}
      </span>
      {rest}
    </>
  );
};

/** A strip of medieval fresco — alternating vermillion, azure, gold diamonds. */
const FrescoStrip: React.FC<{
  orientation: "horizontal" | "vertical";
  length: number;
  thickness: number;
  style: React.CSSProperties;
  vermillion: string;
  azure: string;
  emerald: string;
  gold: string;
}> = ({ orientation, length, thickness, style, vermillion, azure, emerald, gold }) => {
  const horiz = orientation === "horizontal";
  const count = Math.max(4, Math.floor((horiz ? length : length) / (thickness * 1.2)));
  const colors = [vermillion, azure, gold, emerald];
  return (
    <div
      style={{
        position: "absolute",
        width: horiz ? length : thickness,
        height: horiz ? thickness : length,
        display: "flex",
        flexDirection: horiz ? "row" : "column",
        alignItems: "center",
        justifyContent: "center",
        gap: 2,
        overflow: "hidden",
        ...style,
      }}
    >
      {Array.from({ length: count }).map((_, i) => {
        const color = colors[i % colors.length];
        return (
          <svg
            key={i}
            viewBox="0 0 20 20"
            style={{
              width: thickness * 0.75,
              height: thickness * 0.75,
              flexShrink: 0,
            }}
          >
            {/* Diamond */}
            <path
              d="M10 1 L19 10 L10 19 L1 10 Z"
              fill={color}
              stroke="#2A1810"
              strokeWidth="0.8"
            />
            {/* Inner gold dot */}
            <circle cx="10" cy="10" r="2.4" fill={gold} stroke="#3A2A08" strokeWidth="0.5" />
          </svg>
        );
      })}
    </div>
  );
};

/** A painted corner rosette / boss. */
const Rosette: React.FC<{
  gold: string;
  goldLight: string;
  goldDeep: string;
  vermillion: string;
  azure: string;
}> = ({ gold, goldLight, goldDeep, vermillion, azure }) => (
  <svg viewBox="0 0 100 100" style={{ width: "100%", height: "100%" }}>
    <defs>
      <radialGradient id="ros-gold" cx="50%" cy="50%" r="55%">
        <stop offset="0%" stopColor={goldLight} />
        <stop offset="55%" stopColor={gold} />
        <stop offset="100%" stopColor={goldDeep} />
      </radialGradient>
    </defs>
    {/* Outer gold disc */}
    <circle cx="50" cy="50" r="48" fill="url(#ros-gold)" stroke="#3A2A08" strokeWidth="2" />
    {/* 8 petals */}
    {Array.from({ length: 8 }).map((_, i) => {
      const a = (i * 45 * Math.PI) / 180;
      const cx = 50 + Math.cos(a) * 24;
      const cy = 50 + Math.sin(a) * 24;
      return (
        <circle
          key={i}
          cx={cx}
          cy={cy}
          r="10"
          fill={i % 2 === 0 ? vermillion : azure}
          stroke="#1A0C04"
          strokeWidth="1"
        />
      );
    })}
    {/* Center boss */}
    <circle cx="50" cy="50" r="10" fill={goldDeep} stroke="#3A2A08" strokeWidth="1.5" />
    <circle cx="50" cy="50" r="4" fill={goldLight} />
  </svg>
);


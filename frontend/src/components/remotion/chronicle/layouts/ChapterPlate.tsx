import React from "react";
import {
  AbsoluteFill,
  interpolate,
  spring,
  useCurrentFrame,
  useVideoConfig,
} from "remotion";
import type { ChronicleLayoutProps } from "../types";
import {
  CHRONICLE_BODY_FONT,
  CHRONICLE_HEADING_FONT,
} from "../../../../fonts/chronicle-defaults";

/**
 * ChapterPlate — section-title card, redesigned as an **Illuminated Incipit**.
 *
 * Modeled on the opening page of a medieval illuminated manuscript (Book of
 * Kells / Lindisfarne Gospels):
 *   - Heavy painted outer gold frame with a vermillion + azure diamond strip
 *   - Four corner rosettes (gold discs with red/blue petals)
 *   - Inner gold-leaf panel with a sheen sweep and painted corner sprigs
 *   - Optional small-caps *kicker* (from `subtitle`) above the title
 *   - Title in red "rubric" ink with gold drop-shadow
 *   - Painted sprig divider below the title
 *   - Body paragraph (the scene's `narration` / "display text") in italic
 *     brown ink, centered, with its first letter slightly enlarged in
 *     vermillion as a whisper of an illuminated initial.
 *
 * - NO "Chapter", "Chapter N", or Roman numeral content.
 * - Strips any stray "Chapter N:" / "Ch. N:" prefix that may have been
 *   generated by the LLM for older videos, so old data looks clean too.
 */
export const ChapterPlate: React.FC<ChronicleLayoutProps> = ({
  title = "The Story Continues",
  narration,
  subtitle,
  accentColor = "#B8860B",
  textColor = "#2A1810",
  aspectRatio = "landscape",
  titleFontSize,
  descriptionFontSize,
  fontFamily,
}) => {
  const frame = useCurrentFrame();
  const { durationInFrames, height, width, fps } = useVideoConfig();
  const p = aspectRatio === "portrait" || height > width;

  // ── Defensive title cleanup — strip "Chapter N:", "Ch. N:", "Chapter I:",
  // "Chapter One:", etc. off the front of older LLM-generated titles.
  const cleanTitle = stripChapterPrefix(title);

  // ── Timing ─────────────────────────────────────────────
  const FRAME_PAINT_START = 0;
  const FRAME_PAINT_END = 24;
  const PANEL_IN_START = 16;
  const PANEL_IN_END = 34;
  const TITLE_IN_START = 30;
  const DIVIDER_IN = 52;
  const CAPTION_IN_START = 60;

  // Frame paints in (top → right → bottom → left)
  const framePaint = interpolate(
    frame,
    [FRAME_PAINT_START, FRAME_PAINT_END],
    [0, 1],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // Inner panel fades + scales up
  const panelSpring = spring({
    frame: frame - PANEL_IN_START,
    fps,
    config: { damping: 15, stiffness: 110, mass: 1 },
  });
  const panelOp = interpolate(
    frame,
    [PANEL_IN_START, PANEL_IN_END],
    [0, 1],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // Title reveals
  const titleOp = interpolate(
    frame,
    [TITLE_IN_START, TITLE_IN_START + 18],
    [0, 1],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );
  const titleRise = interpolate(
    frame,
    [TITLE_IN_START, TITLE_IN_START + 22],
    [20, 0],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // Sprig divider paints in
  const dividerProgress = interpolate(
    frame,
    [DIVIDER_IN, DIVIDER_IN + 20],
    [0, 1],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // Caption fades in
  const captionOp = interpolate(
    frame,
    [CAPTION_IN_START, CAPTION_IN_START + 22],
    [0, 1],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // Outro fade
  const outroFade = interpolate(
    frame,
    [durationInFrames - 18, durationInFrames],
    [1, 0],
    { extrapolateLeft: "clamp", extrapolateRight: "clamp" },
  );

  // ── Geometry ────────────────────────────────────────────
  const pageW = p ? width * 0.84 : width * 0.72;
  const pageH = p ? height * 0.76 : height * 0.82;
  const frameInset = p ? 28 : 36; // thickness of the painted border band
  const cornerRosette = p ? 64 : 84;

  // ── Palette ─────────────────────────────────────────────
  const GOLD = accentColor;
  const GOLD_LIGHT = lightenHex(accentColor, 0.18);
  const GOLD_DEEP = darkenHex(accentColor, 0.22);
  const VERMILLION = "#9E2B1D"; // rubric red
  const AZURE = "#1F3C74"; // lapis blue
  const EMERALD = "#2F5D3A";
  const INK = textColor;
  const PARCH_BRIGHT = "#F7ECD0";

  const heading = fontFamily ?? CHRONICLE_HEADING_FONT;
  const body = fontFamily ?? CHRONICLE_BODY_FONT;

  // Narration is the *body* of the illuminated page. Subtitle (if given and
  // distinct from the title) becomes a small small-caps kicker above the title.
  const bodyText = (narration ?? "").trim();
  const rawSubtitle = (subtitle ?? "").trim();
  const kickerText =
    rawSubtitle &&
    rawSubtitle.toLowerCase() !== cleanTitle.toLowerCase() &&
    rawSubtitle.toLowerCase() !== title.trim().toLowerCase() &&
    rawSubtitle.toLowerCase() !== bodyText.toLowerCase()
      ? rawSubtitle
      : "";

  const titleFont = titleFontSize ?? computeTitleFont(cleanTitle, pageW, p);
  const bodyFontSize = descriptionFontSize ?? (p ? 32 : 28);
  const kickerFontSize = Math.max(14, (descriptionFontSize ?? (p ? 22 : 20)) * 0.7);

  return (
    <AbsoluteFill
      style={{
        opacity: outroFade,
        alignItems: "center",
        justifyContent: "center",
        fontFamily: body,
      }}
    >
      {/* Outer illuminated page */}
      <div
        style={{
          position: "relative",
          width: pageW,
          height: pageH,
          transform: `scale(${0.96 + 0.04 * Math.min(1, framePaint * 1.2)})`,
          filter: "drop-shadow(0 28px 40px rgba(20,10,4,0.35))",
        }}
      >
        {/* ─────── Painted outer border (gold + diamond fresco) ─────── */}
        <div
          style={{
            position: "absolute",
            inset: 0,
            background: `
              linear-gradient(to right,
                rgba(0,0,0,0.2) 0%,
                rgba(255,220,150,0.15) 30%,
                rgba(0,0,0,0) 60%,
                rgba(255,220,150,0.1) 90%,
                rgba(0,0,0,0.2) 100%),
              linear-gradient(135deg, ${GOLD_LIGHT} 0%, ${GOLD} 35%, ${GOLD_DEEP} 100%)
            `,
            boxShadow: `
              inset 0 0 0 2px #3A2A08,
              inset 0 0 0 3px ${GOLD_DEEP},
              0 0 0 2px #3A2A08
            `,
            clipPath: paintedFrameMask(framePaint),
          }}
        />

        {/* Painted diamond pattern strip inside the gold frame */}
        <div
          style={{
            position: "absolute",
            top: frameInset * 0.45,
            left: frameInset * 0.45,
            right: frameInset * 0.45,
            bottom: frameInset * 0.45,
            pointerEvents: "none",
            opacity: framePaint,
          }}
        >
          {/* Top strip */}
          <FrescoStrip
            orientation="horizontal"
            length={pageW - frameInset * 0.9}
            thickness={frameInset * 0.8}
            style={{ top: 0, left: 0 }}
            vermillion={VERMILLION}
            azure={AZURE}
            emerald={EMERALD}
            gold={GOLD}
          />
          {/* Bottom strip */}
          <FrescoStrip
            orientation="horizontal"
            length={pageW - frameInset * 0.9}
            thickness={frameInset * 0.8}
            style={{ bottom: 0, left: 0 }}
            vermillion={VERMILLION}
            azure={AZURE}
            emerald={EMERALD}
            gold={GOLD}
          />
          {/* Left strip */}
          <FrescoStrip
            orientation="vertical"
            length={pageH - frameInset * 0.9}
            thickness={frameInset * 0.8}
            style={{ top: 0, left: 0 }}
            vermillion={VERMILLION}
            azure={AZURE}
            emerald={EMERALD}
            gold={GOLD}
          />
          {/* Right strip */}
          <FrescoStrip
            orientation="vertical"
            length={pageH - frameInset * 0.9}
            thickness={frameInset * 0.8}
            style={{ top: 0, right: 0 }}
            vermillion={VERMILLION}
            azure={AZURE}
            emerald={EMERALD}
            gold={GOLD}
          />
        </div>

        {/* ─────── Corner rosettes (decorative bosses) ─────── */}
        {[
          { top: -cornerRosette / 2.4, left: -cornerRosette / 2.4 },
          { top: -cornerRosette / 2.4, right: -cornerRosette / 2.4 },
          { bottom: -cornerRosette / 2.4, left: -cornerRosette / 2.4 },
          { bottom: -cornerRosette / 2.4, right: -cornerRosette / 2.4 },
        ].map((pos, i) => (
          <div
            key={`rosette-${i}`}
            style={{
              position: "absolute",
              width: cornerRosette,
              height: cornerRosette,
              opacity: Math.max(
                0,
                Math.min(1, framePaint * 1.4 - i * 0.08),
              ),
              transform: `scale(${Math.min(1, framePaint * 1.2)})`,
              ...pos,
              zIndex: 3,
            }}
          >
            <Rosette
              gold={GOLD}
              goldLight={GOLD_LIGHT}
              goldDeep={GOLD_DEEP}
              vermillion={VERMILLION}
              azure={AZURE}
            />
          </div>
        ))}

        {/* ─────── Inner panel (gold leaf + painted field for title) ─────── */}
        <div
          style={{
            position: "absolute",
            top: frameInset * 1.25,
            left: frameInset * 1.25,
            right: frameInset * 1.25,
            bottom: frameInset * 1.25,
            background: `
              radial-gradient(ellipse at 30% 20%, rgba(255,230,170,0.45) 0%, rgba(255,230,170,0) 55%),
              radial-gradient(ellipse at center, ${PARCH_BRIGHT} 0%, #EDDFB8 65%, #D9C488 100%)
            `,
            boxShadow: `
              inset 0 0 0 1px ${GOLD_DEEP},
              inset 0 0 0 3px #E9D097,
              inset 0 0 60px rgba(184,134,11,0.22),
              inset 0 0 140px rgba(50,25,10,0.15)
            `,
            opacity: panelOp,
            transform: `scale(${interpolate(panelSpring, [0, 1], [0.96, 1])})`,
            transformOrigin: "center center",
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
            justifyContent: "center",
            padding: p ? "10% 8%" : "8% 10%",
            overflow: "hidden",
          }}
        >
          {/* Subtle gold-leaf shimmer sheen moving across the panel */}
          <div
            style={{
              position: "absolute",
              inset: 0,
              background: `linear-gradient(115deg,
                rgba(255,255,220,0) 35%,
                rgba(255,240,180,0.25) 50%,
                rgba(255,255,220,0) 65%
              )`,
              transform: `translateX(${interpolate(frame, [PANEL_IN_END, PANEL_IN_END + 60], [-200, 200], { extrapolateLeft: "clamp", extrapolateRight: "clamp" })}%)`,
              mixBlendMode: "overlay",
              pointerEvents: "none",
            }}
          />

          {/* Small painted sprig decorations at the four inner corners */}
          {(
            [
              { corner: "tl", rot: 0 },
              { corner: "tr", rot: 90 },
              { corner: "br", rot: 180 },
              { corner: "bl", rot: 270 },
            ] as const
          ).map(({ corner, rot }) => {
            const gap = p ? 18 : 24;
            const pos: React.CSSProperties =
              corner === "tl"
                ? { top: gap, left: gap }
                : corner === "tr"
                  ? { top: gap, right: gap }
                  : corner === "br"
                    ? { bottom: gap, right: gap }
                    : { bottom: gap, left: gap };
            return (
              <div
                key={`sprig-${corner}`}
                style={{
                  position: "absolute",
                  width: p ? 52 : 62,
                  height: p ? 52 : 62,
                  transform: `rotate(${rot}deg)`,
                  opacity: panelOp * 0.85,
                  ...pos,
                }}
              >
                <PaintedSprig
                  gold={GOLD_DEEP}
                  vermillion={VERMILLION}
                  azure={AZURE}
                />
              </div>
            );
          })}

          {/* ─────── Optional kicker (small-caps super-title) ─────── */}
          {kickerText && (
            <div
              style={{
                fontFamily: body,
                fontSize: kickerFontSize,
                color: GOLD_DEEP,
                letterSpacing: "0.42em",
                textTransform: "uppercase",
                opacity: titleOp * 0.85,
                marginBottom: p ? 10 : 14,
                zIndex: 2,
              }}
            >
              {kickerText}
            </div>
          )}

          {/* ─────── Title (rubric red with gold shadow) ─────── */}
          <div
            style={{
              fontFamily: heading,
              fontWeight: 900,
              fontSize: titleFont,
              lineHeight: 1.05,
              letterSpacing: "0.02em",
              textAlign: "center",
              color: VERMILLION,
              opacity: titleOp,
              transform: `translateY(${titleRise}px)`,
              textShadow: `
                2px 2px 0 ${GOLD},
                3px 3px 0 ${GOLD_DEEP},
                4px 4px 6px rgba(0,0,0,0.25)
              `,
              maxWidth: "92%",
              textTransform: "uppercase",
              zIndex: 2,
            }}
          >
            {cleanTitle}
          </div>

          {/* ─────── Painted sprig divider below title ─────── */}
          <div
            style={{
              marginTop: p ? 20 : 26,
              marginBottom: p ? 18 : 24,
              width: p ? "74%" : "56%",
              height: p ? 24 : 28,
              opacity: Math.max(0, dividerProgress),
              zIndex: 2,
            }}
          >
            <SprigDivider
              progress={dividerProgress}
              gold={GOLD}
              goldDeep={GOLD_DEEP}
              vermillion={VERMILLION}
            />
          </div>

          {/* ─────── Narration body — the "display text" of the scene ─────── */}
          {bodyText && (
            <div
              style={{
                fontFamily: body,
                fontStyle: "italic",
                fontSize: bodyFontSize,
                color: INK,
                opacity: captionOp,
                textAlign: "center",
                lineHeight: 1.55,
                maxWidth: p ? "88%" : "78%",
                letterSpacing: "0.01em",
                zIndex: 2,
                textShadow: "0 1px 0 rgba(255,235,180,0.4)",
              }}
            >
              <span
                style={{
                  // First letter slightly enlarged — a whisper of a drop cap,
                  // in keeping with the manuscript aesthetic.
                  display: "inline",
                }}
              >
                <BodyWithFirstLetter text={bodyText} accent={VERMILLION} />
              </span>
            </div>
          )}
        </div>
      </div>
    </AbsoluteFill>
  );
};

